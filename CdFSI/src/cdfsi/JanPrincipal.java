    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String Titulo = txtTitulo.getText();
        String Genero = txtGenero.getText();
        int Duracao = Integer.parseInt(txtDuracao.getText());
        String Diretor = txtNDiretor.getText();
        int Ano = Integer.parseInt(txtALancamento.getText());
        Icon Imagem = jLabel8.getIcon();

         if (radioF.isSelected()==true && radioS.isSelected()==false) {
            Filme NFilme = new Filme(Titulo, Genero, Duracao, Diretor, Ano, Imagem);
            rep.InsereF(NFilme);
            JOptionPane.showMessageDialog(null, "Filme cadastrado com sucesso", "Cadastro com exito", 2);
        }else if(radioF.isSelected()==false && radioS.isSelected()==true) {
            Serie NSerie = new Serie(Titulo, Genero, Duracao, Diretor, Ano, Imagem);
            rep.InsereS(NSerie);
            JOptionPane.showMessageDialog(null, "Serie cadastrada com sucesso", "Cadastro com exito", 2);
        }
        
        txtTitulo.setText("");
        txtGenero.setText("");
        txtDuracao.setText("");
        txtNDiretor.setText("");
        txtALancamento.setText("");
        jLabel8.setIcon(null);
        buttonGroup1.clearSelection();
       
        
        
    }//GEN-LAST:event_btnAddActionPerformed


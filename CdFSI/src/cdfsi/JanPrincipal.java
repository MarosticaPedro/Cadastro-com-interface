    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int cdg =  Integer.parseInt(JOptionPane.showInputDialog(null, "Digite: \n 1 para excluir filme \n 2 para excluir serie . "));
             
        switch (cdg) {
            case 1:
                    int idx =  Integer.parseInt(JOptionPane.showInputDialog(null, "Digite o codigo do filme que deseja excluir"));
                
                    rep.removeF(idx);
                break;
                
            case 2:
                    idx =  Integer.parseInt(JOptionPane.showInputDialog(null, "Digite o codigo da serie que deseja excluir"));
                
                    rep.removeS(idx);
                break;
                
            default:
                throw new AssertionError();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarActionPerformed
        // TODO add your handling code here:
        int cdg =  Integer.parseInt(JOptionPane.showInputDialog(null, "Digite: \n 1 para visualizar filme \n 2 para visualizar serie . "));
             
        switch (cdg) {
            case 1:
                    int idx1 =  Integer.parseInt(JOptionPane.showInputDialog(null, "Digite o codigo do filme que deseja visualizar"));
                    JanelaVisu.exibeF(this, idx1);
                break;
                
            case 2:
                    int idx2 =  Integer.parseInt(JOptionPane.showInputDialog(null, "Digite o codigo da serie que deseja visualizar"));
                    JanelaVisu.exibeS(this, idx2);
                break;
                
            default:
                throw new AssertionError();
        }
         if(JanelaVisu==null){
            JanelaVisu = new JanVisu();
            JanelaVisu.setLocationRelativeTo(null);
            JanelaVisu.setVisible(true);
            JanelaVisu.setResizable(false);
        }else{
        JanelaVisu.setLocationRelativeTo(null);
            JanelaVisu.setVisible(true);
            JanelaVisu.setResizable(false);
        }
    }//GEN-LAST:event_btnVisualizarActionPerformed

